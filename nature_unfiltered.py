def pick_card_type() -> str:
    forced = os.environ.get("CARD_TYPE")
    if forced:
        if forced not in CARD_TYPES:
            print(f"[ERROR] Unknown CARD_TYPE '{forced}'. Available: {', '.join(CARD_TYPES)}")
            sys.exit(1)
        return forced

    STATE_DIR.mkdir(exist_ok=True)
    order = list(CARD_TYPES.keys())

    last_index = -1
    if os.path.exists(CARD_STATE_FILE):
        try:
            with open(CARD_STATE_FILE) as f:
                state = json.load(f)
            last_key = state.get("last_card")
            if last_key in order:
                last_index = order.index(last_key)
            else:
                last_index = state.get("last_index", -1)
        except Exception:
            pass

    next_index = (last_index + 1) % len(order)
    card_key = order[next_index]

    with open(CARD_STATE_FILE, "w") as f:
        json.dump({"last_card": card_key, "last_index": next_index}, f)

    return card_key


