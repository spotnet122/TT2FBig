def generate_content(card_key: str) -> tuple:
    card = CARD_TYPES[card_key]

    if not GROQ_API_KEY:
        print("[INFO] GROQ_API_KEY not set — using fallback sample content.")
        return FALLBACK_CONTENT[card_key], FALLBACK_IMAGE_PROMPTS[card_key]

    angle_part = ""
    if card["use_angle"]:
        angle = random.choice(CAPTION_ANGLES)
        angle_part = f"\nStyle: {angle}."

    prompt = (
        f"You are writing a highly engaging Facebook image-card for a "
        f"travel & nature page.\n\n"
        f"{card['instruction']}{angle_part}\n\n"
        f"Also write a short photographic scene description (for an AI image "
        f"generator) that visually matches this content — real Earth nature "
        f"or travel scenery, cinematic travel-photography style, no text, no "
        f"logos, no watermarks, no close-up human faces. Under 22 words.\n\n"
        f"Respond with ONLY valid JSON, nothing else, no markdown fences, in "
        f'exactly this format: {{"text": "...", "image_prompt": "..."}}\n\n'
        f"Rules for 'text': English, under {card['max_chars']} characters, "
        f"no hashtags, no emojis, no quotation marks, no explanation — just "
        f"the line itself. Must be about a REAL place/nature/geography, "
        f"never fictional. Do not reveal a true/false answer. Avoid vague, "
        f"generic filler phrases (e.g. 'a beautiful place', 'amazing "
        f"views', 'stunning scenery') — every line must contain at least "
        f"one specific, concrete, information-rich detail."
    )

    try:
        resp = requests.post(
            "https://api.groq.com/openai/v1/chat/completions",
            headers={"Authorization": f"Bearer {GROQ_API_KEY}", "Content-Type": "application/json"},
            json={
                "model": IMAGE_GROQ_MODEL,
                "messages": [{"role": "user", "content": prompt}],
                "temperature": 1.0,
                "max_tokens": 220,
            },
            timeout=30,
        )
        resp.raise_for_status()
        raw = resp.json()["choices"][0]["message"]["content"].strip()
        raw = raw.strip("`")
        if raw.lower().startswith("json"):
            raw = raw[4:].strip()

        match = re.search(r"\{.*\}", raw, re.DOTALL)
        if match:
            raw = match.group(0)

        data = json.loads(raw)

        text = str(data.get("text", "")).strip().strip('"').strip("'").strip()
        image_prompt = str(data.get("image_prompt", "")).strip()

        max_len = card["max_chars"] + 40
        if len(text) > max_len:
            text = text[:max_len].rsplit(" ", 1)[0] + "..."

        if not text:
            text = FALLBACK_CONTENT[card_key]
        if not image_prompt:
            image_prompt = FALLBACK_IMAGE_PROMPTS[card_key]

        return text, image_prompt
    except Exception as e:
        print(f"[WARN] Groq content generation failed ({e}), using fallback.")
        return FALLBACK_CONTENT[card_key], FALLBACK_IMAGE_PROMPTS[card_key]


def lerp(a, b, t):
    return tuple(int(a[i] + (b[i] - a[i]) * t) for i in range(3))


def draw_gradient(size, top_color, bottom_color, diagonal=True):
    img = Image.new("RGB", (size, size), top_color)
    draw = ImageDraw.Draw(img)
    if diagonal:
        for y in range(size):
            t = y / size
            row_color = lerp(top_color, bottom_color, t)
            draw.line([(0, y), (size, y)], fill=row_color)
        overlay = Image.new("L", (size, size), 0)
        odraw = ImageDraw.Draw(overlay)
        for x in range(size):
            odraw.line([(x, 0), (0, x)], fill=int(30 * (x / size)))
        dark = Image.new("RGB", (size, size), (0, 0, 0))
        img = Image.composite(dark, img, overlay.point(lambda p: int(p * 0.5)))
    else:
        for y in range(size):
            t = y / size
            draw.line([(0, y), (size, y)], fill=lerp(top_color, bottom_color, t))
    return img


def build_gradient_background(card: dict) -> Image.Image:
    top_c, bottom_c = card["gradient"]
    img = draw_gradient(IMG_SIZE, top_c, bottom_c, diagonal=True)

    vignette = Image.new("L", (IMG_SIZE, IMG_SIZE), 0)
    vdraw = ImageDraw.Draw(vignette)
    vdraw.ellipse([-200, -200, IMG_SIZE + 200, IMG_SIZE + 200], fill=255)
    vignette = vignette.filter(ImageFilter.GaussianBlur(180))
    dark_layer = Image.new("RGB", (IMG_SIZE, IMG_SIZE), (0, 0, 0))
    img = Image.composite(img, dark_layer, vignette)
    return img


def crop_to_square(img: Image.Image, size: int) -> Image.Image:
    img = img.convert("RGB")
    w, h = img.size
    scale = size / min(w, h)
    new_w, new_h = int(w * scale) + 1, int(h * scale) + 1
    img = img.resize((new_w, new_h))
    left = (new_w - size) // 2
    top = (new_h - size) // 2
    return img.crop((left, top, left + size, top + size))


def apply_photo_overlay(img: Image.Image) -> Image.Image:
    img = img.convert("RGB")
    overlay = Image.new("L", (IMG_SIZE, IMG_SIZE), 85)
    odraw = ImageDraw.Draw(overlay)

    top_band = 360
    for y in range(top_band):
        alpha = int(190 * (1 - y / top_band))
        odraw.line([(0, y), (IMG_SIZE, y)], fill=max(alpha, 60))

    bottom_band = 380
    start_y = IMG_SIZE - bottom_band
    for y in range(start_y, IMG_SIZE):
        t = (y - start_y) / bottom_band
        alpha = int(200 * t)
        odraw.line([(0, y), (IMG_SIZE, y)], fill=max(alpha, 60))

    black = Image.new("RGB", (IMG_SIZE, IMG_SIZE), (0, 0, 0))
    return Image.composite(black, img, overlay)


def fetch_background_photo(image_prompt: str):
    prompt_enc = urllib.parse.quote(image_prompt + IMAGE_STYLE_SUFFIX)
    url = f"{POLLINATIONS_BASE}/{prompt_enc}"

    for attempt in range(1, POLLINATIONS_RETRIES + 1):
        try:
            seed = random.randint(1, 999999)
            resp = requests.get(
                url,
                params={"width": IMG_SIZE, "height": IMG_SIZE, "nologo": "true",
                        "seed": seed, "model": POLLINATIONS_MODEL},
                timeout=POLLINATIONS_TIMEOUT,
            )
            resp.raise_for_status()
            img = Image.open(io.BytesIO(resp.content))
            return crop_to_square(img, IMG_SIZE)
        except Exception as e:
            print(f"[WARN] Pollinations attempt {attempt}/{POLLINATIONS_RETRIES} failed: {e}")
            time.sleep(3)

    print("[WARN] Pollinations image fetch failed — falling back to gradient background.")
    return None


def wrap_text(draw, text, fnt, max_width):
    words = text.split()
    lines, current = [], ""
    for word in words:
        trial = f"{current} {word}".strip()
        bbox = draw.textbbox((0, 0), trial, font=fnt)
        if bbox[2] - bbox[0] <= max_width or not current:
            current = trial
        else:
            lines.append(current)
            current = word
    if current:
        lines.append(current)
    return lines


def draw_centered_multiline(draw, lines, fnt, center_x, center_y, fill, line_spacing=1.35, shadow=True):
    bboxes = [draw.textbbox((0, 0), line, font=fnt) for line in lines]
    heights = [b[3] - b[1] for b in bboxes]
    line_height = max(heights) * line_spacing
    total_height = line_height * len(lines)
    y = center_y - total_height / 2
    for line, bbox in zip(lines, bboxes):
        w = bbox[2] - bbox[0]
        x = center_x - w / 2
        if shadow:
            draw.text((x + 2, y + 3), line, font=fnt, fill=(0, 0, 0))
        draw.text((x, y), line, font=fnt, fill=fill)
        y += line_height
    return y


def draw_centered_single(draw, text, fnt, center_x, y, fill, shadow=True):
    bbox = draw.textbbox((0, 0), text, font=fnt)
    w = bbox[2] - bbox[0]
    x = center_x - w / 2
    if shadow:
        draw.text((x + 1, y + 2), text, font=fnt, fill=(0, 0, 0))
    draw.text((x, y), text, font=fnt, fill=fill)


def deco_compass(draw, accent):
    cx, cy = IMG_SIZE // 2, 150
    r = 46
    draw.ellipse([cx - r, cy - r, cx + r, cy + r], outline=accent, width=3)
    for ang in (0, 90, 180, 270):
        rad = math.radians(ang - 90)
        x2 = cx + (r + 16) * math.cos(rad)
        y2 = cy + (r + 16) * math.sin(rad)
        draw.line([(cx, cy), (x2, y2)], fill=accent, width=3)
    draw.polygon([(cx, cy - r + 8), (cx - 9, cy), (cx, cy + r - 8), (cx + 9, cy)], fill=accent)
    for cx2, cy2 in [(90, 90), (IMG_SIZE - 90, 90)]:
        draw.ellipse([cx2 - 6, cy2 - 6, cx2 + 6, cy2 + 6], fill=accent)


def deco_lightning_split(draw, accent):
    def bolt(cx, cy, s):
        pts = [(cx, cy - s), (cx - s * 0.5, cy + s * 0.15), (cx, cy + s * 0.15),
               (cx - s * 0.2, cy + s), (cx + s * 0.5, cy - s * 0.15), (cx, cy - s * 0.15)]
        draw.polygon(pts, fill=accent)

    bolt(150, 150, 34)
    bolt(IMG_SIZE - 150, 150, 34)
    cx, cy = IMG_SIZE // 2, 150
    draw.ellipse([cx - 34, cy - 34, cx + 34, cy + 34], outline=accent, width=3)
    vs_font = font("Poppins-Bold.ttf", 26)
    draw_centered_single(draw, "VS", vs_font, cx, cy - 16, accent, shadow=False)


